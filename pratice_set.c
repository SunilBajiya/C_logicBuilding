{
   
   for(int i =1;i<=6;i++){
       for(int s=6; s>=i;s--){
           printf(" ");
       }
       for(int j = 1; j<=i;j++){
           printf("* ");
       }
       printf("\n");
   }
   for(int i=1 ; i<=5;i++){
       for(int j =1; j<=12;j++){
           if(i==1 || j==1 || i==5 || j==12){
               printf("*");
           }
           else{
               printf(" ");
           }
       }
       printf("\n");
   }

    return 0;
}


